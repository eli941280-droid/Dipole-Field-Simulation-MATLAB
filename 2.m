clear all; % 清除所有变量，避免干扰

% 用户输入部分
a = input('输入点电荷的位置如[1,0;2,0]表示位置在(1,0),(2,0)：'); 
% a是电荷的位置，用户输入电荷的坐标，例如[1,0;2,0]表示电荷分别在(1,0)和(2,0)

Q = input('输入点电荷的电荷量，+-表示电性，如[1,-1]：'); 
% Q是电荷的量，正电荷为正数，负电荷为负数。例如，[1,-1]表示正电荷为+1，负电荷为-1

a1 = input('电场线角度间隔：'); 
% a1是电场线的角度间隔，决定电场线的密度。较小的a1表示更多的电场线，密度更大

t = input('等势面差值间隔（可选择调小间隔或者成倍增加电荷量，如0.01）：'); 
% t是等势面差值的间隔，较小的t值会使得等势线更加密集，展示更细致的电势分布

q = 1; % 电量比，设置为1，实际应用中可以根据需要调整

% 设置坐标范围，留出一些空白边界
xmin = min(a(:,1)) - 4; % x坐标范围的最小值，留4个单位的空白
xmax = max(a(:,1)) + 4; % x坐标范围的最大值，留4个单位的空白
ymin = min(a(:,2)) - 4; % y坐标范围的最小值，留4个单位的空白
ymax = max(a(:,2)) + 4; % y坐标范围的最大值，留4个单位的空白

x = linspace(xmin, xmax); % 创建横坐标向量
y = linspace(ymin, ymax); % 创建纵坐标向量
[X, Y] = meshgrid(x, y); % 创建网格坐标，X和Y是对应的网格点坐标矩阵

g = size(a, 1); % 获取电荷数量，a的行数即为电荷个数
R = {}; % 初始化一个空的cell数组，用于存储电荷到每个网格点的距离

% 计算每个网格点到所有电荷的距离
for i = 1:g
    R{1,i} = sqrt((X - a(i,1)).^2 + (Y - a(i,2)).^2); % 计算电荷位置到网格点的欧几里得距离
end

U = zeros(size(X)); % 初始化电势矩阵，U的大小与网格矩阵X和Y相同，初始值为0
% 计算电势
for i = 1:g
    U = U + Q(1,i) * (1 ./ R{1,i}); % 根据每个电荷的电量计算电势并叠加
end

min1 = min(min(U)); % 找到电势矩阵U中的最小值
max1 = max(max(U)); % 找到电势矩阵U中的最大值
u = min1:t:max1; % 根据电势最小值、最大值和差值间隔t生成等势线的电势向量

% 创建图形窗口并绘制等势线
figure; % 创建一个新的图形窗口
contour(X, Y, U, u, '--', 'LineColor', 'k'); % 绘制等势线，'--'表示虚线，'k'表示黑色
hold on; % 保持图像，使得之后的绘图操作会添加到同一个图像上

% 绘制电荷点
for i = 1:g
    if Q(1,i) > 0
        % 如果电荷量大于0，绘制正电荷
        plot(a(i,1), a(i,2), 'o', 'MarkerSize', 16, 'MarkerEdgeColor', 'r', 'MarkerFaceColor', 'r'); 
        % 使用红色实心圆表示正电荷
    else
        % 如果电荷量小于0，绘制负电荷
        plot(a(i,1), a(i,2), 'o', 'MarkerSize', 16, 'MarkerEdgeColor', 'b', 'MarkerFaceColor', 'b'); 
        % 使用蓝色实心圆表示负电荷
    end
end

% 计算电场强度的分量
[Ex, Ey] = gradient(-U, x(2)-x(1), y(2)-y(1)); 
% 使用电势的负梯度来计算电场的x和y分量。梯度计算给出场强的分布

r0 = 0.1; % 设置电场线的起点半径，控制电场线的起始位置
b = (a1:a1:360-a1) * pi / 180; % 电场线的始末角度和步长，将角度转换为弧度

% 绘制电场线
for i = 1:g
    if Q(1,i) > 0
        factor = 1; % 正电荷产生的电场线方向
        color = 'r'; % 红色表示正电荷的电场线
    else
        factor = -1; % 负电荷产生的电场线方向
        color = 'b'; % 蓝色表示负电荷的电场线
    end
    
    % 根据电荷位置和电场线起点半径计算电场线的起始点
    x1{1,i} = r0 * cos(b) + a(i,1); % 电场线起始点的横坐标
    y1{1,i} = r0 * sin(b) + a(i,2); % 电场线起始点的纵坐标
    
    % 使用streamline函数绘制电场线
    h = streamline(X, Y, factor * Ex, factor * Ey, x1{1,i}, y1{1,i}); 
    % factor决定电场线的方向，正电荷使用factor=1，负电荷使用factor=-1
    set(h, 'Color', color); % 设置电场线的颜色
    hold on; % 保持图像
end

% 设置图像的显示属性
axis equal tight; % 保持坐标刻度相等并紧凑显示
title('二维平面电偶极子的电场线和等势', 'fontsize', 16); % 图像标题
xlabel('\itx/r (电势单位:kq/r=1)', 'fontsize', 12); % x轴标签
ylabel('\ity/r', 'fontsize', 12); % y轴标签
